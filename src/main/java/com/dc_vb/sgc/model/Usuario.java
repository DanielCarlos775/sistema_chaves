package com.dc_vb.sgc.model;

public class Usuario {

    private int id;               // PK, auto_increment
    private String nome;          // nome completo
    private String email;         // usado no login
    private String senha;         // hash da senha
    private String tipoUsuario;   // ADM, COORDENADOR, GUARITA, etc.

    // Getters e Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getTipoUsuario() {
        return tipoUsuario;
    }

    public void setTipoUsuario(String tipoUsuario) {
        this.tipoUsuario = tipoUsuario;
    }
}